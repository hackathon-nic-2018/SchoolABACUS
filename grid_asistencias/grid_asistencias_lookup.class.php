   function lookup_asistencia(&$asistencia) 
   {
      $conteudo = "" ; 
      if ($asistencia == "1")
      { 
          $conteudo = "Presente";
      } 
      if ($asistencia == "0")
      { 
          $conteudo = "Ausente";
      } 
      if ($asistencia == "0.5")
      { 
          $conteudo = "Tarde";
      } 
      if (!empty($conteudo)) 
      { 
          $asistencia = $conteudo; 
      } 
   }  
